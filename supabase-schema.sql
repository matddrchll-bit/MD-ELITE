create table public.admin_users (
  email text primary key check (email = lower(email)),
  created_at timestamptz not null default now()
);

create table public.products (
  id bigint generated by default as identity primary key,
  category text not null check (category in ('teclados', 'mouse', 'audifonos', 'mousepads', 'camaras', 'accesorios')),
  emoji text not null default '📦',
  name text not null check (char_length(name) between 1 and 160),
  description text not null default '',
  price numeric(12,2) not null check (price >= 0),
  stock integer not null default 0 check (stock >= 0),
  badge text check (badge is null or badge in ('new', 'hot')),
  image_url text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.offers (
  id uuid primary key default gen_random_uuid(),
  product_id bigint not null references public.products(id) on delete cascade,
  old_price numeric(12,2) not null check (old_price > 0),
  offer_price numeric(12,2) not null check (offer_price > 0 and offer_price < old_price),
  discount_percent integer not null check (discount_percent between 1 and 99),
  start_date date,
  end_date date,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (start_date is null or end_date is null or start_date <= end_date)
);

create index offers_product_id_idx on public.offers(product_id);
create index offers_dates_idx on public.offers(start_date, end_date);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create trigger products_set_updated_at
before update on public.products
for each row execute function public.set_updated_at();

create trigger offers_set_updated_at
before update on public.offers
for each row execute function public.set_updated_at();

